const getFileName = (path) => path.split("/").pop();

const sortImagesForGallery = (home) => {
  const exteriorOrder = new Map((exteriorImagesByHome[home.title] || []).map((fileName, index) => [fileName, index]));
  const planOrder = new Map((planImagesByHome[home.title] || []).map((fileName, index) => [fileName, index]));

  return [...home.images]
    .map((image, index) => {
      const fileName = getFileName(image);
      const isPlan = planOrder.has(fileName) || /(?:_etazh|этаж|plan|план)/i.test(fileName);
      const category = isPlan ? 2 : exteriorOrder.has(fileName) ? 0 : 1;
      const order = category === 0 ? exteriorOrder.get(fileName) : category === 2 ? planOrder.get(fileName) : index;

      return { image, index, category, order: order ?? index };
    })
    .sort((a, b) => a.category - b.category || a.order - b.order || a.index - b.index)
    .map((item) => item.image);
};

